//7. Сложная задача. ЗАДАНИЕ. Группу из 20 студентов нужно разделить
// на 3 бригады, причем в первую бригаду должны входить 3 человека,
// во вторую — 5 и в третью — 12. Сколькими способами это можно сделать.
//Написать функцию для решения этой задачи. Задача решается при помощи
//комбинаторики (сочитания и правило умножения).

function C($k, $n)
{
    return fact($n)/(fact($n-$k)*fact($k));
}
echo "Способов сбора первой пригады - ". C(3,20).", второй - ". C(5,20). ", третьей - ". C(12,20);

